# Type casting
fnum = int(input("Enter first num : "))
snum = int(input("Enter second num : "))

addition = fnum + snum
print("Sum is",addition)

subtraction = fnum - snum
print("Sub is",subtraction)

division = fnum / snum
print("Div is {}".format(division))

mul = fnum * snum
# f-strings
print(f"Mul is {mul}")
